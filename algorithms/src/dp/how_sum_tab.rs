pub fn how_sum_tab(target: usize, numbers: Vec<i32>) -> Option<Vec<i32>> {
  let mut tab: Vec<Option<Vec<i32>>> = vec![None; target + 1];

  tab[0] = Some(vec![]);

  (0..=target).for_each(|i| {
    for forward_num in &numbers {
      if let Some(source_arr) = &tab[i] {
        if i + *forward_num as usize <= target {
          let mut forward_arr = source_arr.clone();
          forward_arr.insert(0, *forward_num);
          tab[i + *forward_num as usize] = Some(forward_arr);
        }
      }
    }
  });

  // println!("{:#?}", &tab);

  tab[target].clone()
}

